# -*- encoding: utf-8 -*-
'''
*的使用
0、专门为解压不确定个数或任意个数元素的可迭代对象而设计的
1、将传入的可迭代参数序列解析出来（自动解包）
2、unpack可迭代变量
3、*变量永远是列表类型
'''

def f(a,b,c,d):
	print(a, b, c, d, sep = '&')
f(1, 2, 3, 4)
f(*[1, 2, 3, 4])

# 自动解包
print(*[1, 2, 3, 4])

# 接收任意个元素
def f(*args):
	print(args)
f([1,2,3,4])

a, *b, c = range(5)
print(b)
